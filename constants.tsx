export const DEFAULT_POEMS: Poem[] = Array.from({ length: 15 }).map((_, i) => {
  const angle = 0.5 * i;
  const radius = 8 + (i * 1.8);
  const x = 50 + radius * Math.cos(angle);
  const y = 50 + radius * Math.sin(angle);

  return {
    id: i + 1,
    title: MODEL_TITLES[i % MODEL_TITLES.length],
    poet: MODEL_POETS[i % MODEL_POETS.length],
    image: `https://images.unsplash.com/photo-1455390582262-044cdead277a?auto=format&fit=crop&q=80&w=1000&seed=${i}`,
    audioUrl: `https://www.soundhelix.com/examples/mp3/SoundHelix-Song-${(i % 5) + 1}.mp3`,
    content: MODEL_CONTENTS[i % MODEL_CONTENTS.length],
    x: Math.max(5, Math.min(95, x)),
    y: Math.max(5, Math.min(95, y)),
    z: i * 50
  };
});

